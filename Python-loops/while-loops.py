"""
While loops are structures in Python, which allows us to loop through and execute a block of code multiple times. 
So we can specify different lines of code, and then we can put that code inside of a while loop and it would basically loop through that code, 
executing it repeatedly until a certain condition is false.
"""

i = 1
while i <= 100:
    print(i)
    i = i + 1

print("Loop completed")